exports.GPSMeasureDistance = function(lat1, lon1, lat2, lon2)
{
    var R = 6378.137; // Radius of earth in KM
    var dLat = parseFloat(lat2) * Math.PI / 180 - parseFloat(lat1) * Math.PI / 180;
    var dLon = parseFloat(lon2) * Math.PI / 180 - parseFloat(lon1) * Math.PI / 180;
    var a = Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(parseFloat(lat1) * Math.PI / 180) * Math.cos(parseFloat(lat2) * Math.PI / 180) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
    var c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
    var d = R * c;
    return d * 1000; // meters
}

exports.map_range = function(value, low1, high1, low2, high2)
{
    return low2 + (high2 - low2) * (value - low1) / (high1 - low1);
}

exports.getFareZone = function(estimatedDuration,zoneFare,zoneTimeBlock)
{
  var blockCount = parseInt((estimatedDuration / zoneTimeBlock)) ; //block count must be in interger...
  var blockBalance = (parseInt((estimatedDuration % zoneTimeBlock)) > 0) ? zoneFare : 0 ; //Remainder of block...
  var fare = (blockCount * zoneFare) + blockBalance; // fare is accordingly....based on block charge.

  return({"status":"OK" , "fareInCents":fare});
}

